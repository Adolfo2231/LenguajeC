#include <stdio.h>

int suma(int *pa, int *pb){

    int a = *pa + *pb;

    return a;
}

int resta(int *pa, int *pb)
{

    int a = *pa - *pb;

    return a;
}

int multiplicacion(int *pa, int *pb)
{

    int a = *pa * *pb;

    return a;
}

int division(int *pa, int *pb)
{

    int a = *pa / *pb;

    return a;
}
